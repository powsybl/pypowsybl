from pypowsybl.opf.impl.model.network_cache import NetworkCache


class Constraints(ABC):
    @abstractmethod
    def add(self,
            parameters: ModelParameters,
            network_cache: NetworkCache,
            variable_context: VariableContext,
            model: Model) -> None:
        pass
